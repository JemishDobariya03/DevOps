# Python Datatypes with Examples

# Integer
num = 10
print("Integer:", num)

# Float
price = 19.99
print("Float:", price)

# String
name = "Alice"
print("String:", name)

# Boolean
is_active = True
print("Boolean:", is_active)

# List
fruits = ["apple", "banana", "cherry"]
print("List:", fruits)

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates)

# Dictionary
person = {"name": "Bob", "age": 25}
print("Dictionary:", person)

# Set
unique_numbers = {1, 2, 3, 2}
print("Set:", unique_numbers)